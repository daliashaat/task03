def add(n1,n2):
    return n1+n2
def multipulation(n1,n2):
    return n1*n2
def subtraction (n1,n2):
    return n1-n2
def division (n1,n2):
    return n1/n2

n1 = int(input("Enter number 1:"))
n2 = int(input("Enter number 2:"))

print(add(n1,n2))
print(multipulation(n1,n2))
print(subtraction (n1,n2))
print(division (n1,n2))
